# ─── LOOKUP HELPERS ──────────────────────────────────────────────────────────

_PLACE_BY_ID: dict[int, Place] = {p.id: p for p in PORTO_PLACES}


def get_place_by_id(place_id: int) -> Place | None:
    """Return a place by its ID, or None."""
    return _PLACE_BY_ID.get(place_id)


def get_all_restaurants() -> list[Place]:
    """Return only restaurant and café locations."""
    return [p for p in PORTO_PLACES if p.type in ("restaurant", "café")]
